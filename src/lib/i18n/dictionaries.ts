/** Format a translated string with placeholders ({foo} → value) */
export function format(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => (vars[k] ?? "").toString());
}

/** Look up a translation key for a given locale, fallback chain to en, de. */
export function lookup(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  const value =
    DICT[locale]?.[key] ??
    DICT.en?.[key] ??
    DICT.de?.[key] ??
    key;
  return format(value, vars);
}

/** Detect locale from Accept-Language string */
export function detectLocale(acceptLanguage: string | null | undefined): Locale {
  if (!acceptLanguage) return DEFAULT_LOCALE;
  const lower = acceptLanguage.toLowerCase();
  for (const loc of LOCALES) {
    if (lower.includes(loc + "-") || lower.startsWith(loc)) return loc;
  }
  return DEFAULT_LOCALE;
}
